#were creating a count up from 0-50 with 50 as our limit and 1 as incremnet 
print("Counting up from 0 to 50 in increments of 1:")
for i in range(0, 51, 1):
    print(i, end=" ")
print("\n")

#counting down from 50 to 0 with -1 as our decreament
print("Counting down from 50 to 0 in decrements of 1:")
for i in range(50, -1, -1):
    print(i, end=" ")
print("\n")

